package com.csis3275.Model_group3;

public class Carlos_group3 {
	
	String name;
	int age;
	String program;

	
	
	public Carlos_group3() {
		//default constructor
	}



	public Carlos_group3(String name, int age, String program) {
		super();
		this.name = name;
		this.age = age;
		this.program = program;
	}



	public String getName() {
		return name;
	}



	public void setName(String name) {
		this.name = name;
	}



	public int getAge() {
		return age;
	}



	public void setAge(int age) {
		this.age = age;
	}



	public String getProgram() {
		return program;
	}



	public void setProgram(String program) {
		this.program = program;
	}
	
	
	
	

}
